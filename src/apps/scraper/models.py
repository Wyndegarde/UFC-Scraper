    blue_Height = models.FloatField()
    blue_Weight = models.FloatField()
    blue_Reach = models.FloatField()
    blue_STANCE = models.CharField(max_length=100, default="Orthodox", blank=True)
    blue_DOB = models.CharField(max_length=100)
    blue_SLpM = models.FloatField()
    blue_Str_Acc = models.FloatField()
    blue_SApM = models.FloatField()
    blue_Str_Def = models.FloatField()
    blue_TD_Avg = models.FloatField()
    blue_TD_Acc = models.FloatField()
    blue_TD_Def = models.FloatField()
    blue_Sub_Avg = models.FloatField()
    blue_record = models.CharField(max_length=100)
