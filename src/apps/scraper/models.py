    blue_Td_percent = models.FloatField()
    red_Sub_att = models.IntegerField()
    blue_Sub_att = models.IntegerField()
    red_Rev = models.IntegerField()
    blue_Rev = models.IntegerField()
    red_Ctrl = models.IntegerField()
    blue_Ctrl = models.IntegerField()
    red_Height = models.FloatField()
    red_Weight = models.FloatField()
    red_Reach = models.FloatField()
    red_STANCE = models.CharField(max_length=100, default="Orthodox", blank=True)
    red_DOB = models.CharField(max_length=100)
    red_SLpM = models.FloatField()
    red_Str_Acc = models.FloatField()
    red_SApM = models.FloatField()
    red_Str_Def = models.FloatField()
    red_TD_Avg = models.FloatField()
